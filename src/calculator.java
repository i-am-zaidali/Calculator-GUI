    private boolean verifyNumber(String numStr) {
        try {
            float number = Float.parseFloat(numStr);

            if (number > Float.MAX_VALUE) {
                JOptionPane.showMessageDialog(this, "Error: Number is too large.", "Error", JOptionPane.ERROR_MESSAGE);
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid input: Not a valid integer.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
    
    private String formatFloat(float num) {
        String numStr = Float.toString(num);
        
        if (numStr.contains("E")) {
            return new BigDecimal(num).stripTrailingZeros().toPlainString();
        } else if (numStr.endsWith("0")) {
            return numStr.substring(0, numStr.indexOf("."));
        } else {
            return numStr;
        }
    }
    
