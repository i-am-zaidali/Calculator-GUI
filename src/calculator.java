    private void handleNumber(String num) {
        String orgEq = inputNumberTextBox.getText();
        if(!num.equals(".") || (!orgEq.contains("."))){
            if (orgEq.equals("0") && num.charAt(0) != '.') {
                orgEq = num;
            } else {
                orgEq += num;
            }
            if ('.' != num.charAt(0) && !verifyNumber(orgEq)) {
                inputNumberTextBox.setText("");
            } else {
                inputNumberTextBox.setText(orgEq);
            }
        }
    }
    
    private void handleOp(char op) {
        String orgEq = eqTextBox.getText();
        String orgNum = inputNumberTextBox.getText();
        float num = Float.parseFloat(orgNum);
        if (op == '-' && (orgNum.isEmpty() || orgNum.equals("0"))){
            inputNumberTextBox.setText("-");
            return;
        } else if (op == '-' && orgNum.equals("-")) {
            return;
        }
        inputNumberTextBox.setText("0");
        
        orgEq += String.format("%s %c ", formatFloat(num), op);
        eqTextBox.setText(orgEq);
    }
    
    private void solveEquation() {
        String equation = eqTextBox.getText();
        equation += " "+inputNumberTextBox.getText();
        try{
            ExpressionSolver solver = new ExpressionSolver(equation);
            float answer = solver.solve();
            eqTextBox.setText("");
            inputNumberTextBox.setText(formatFloat(answer));
        } catch (ArithmeticException a){
            JOptionPane.showMessageDialog(this, "Error: Can't divide by zero", "Error", JOptionPane.ERROR_MESSAGE);
            eqTextBox.setText("");
            inputNumberTextBox.setText("0");
        }
    }
    
